def calculate_grade(percentage):
    if percentage >= 90:
        return 'A'
    elif percentage >= 80:
        return 'B'
    elif percentage >= 70:
        return 'C'
    elif percentage >= 60:
        return 'D'
    elif percentage >= 50:
        return 'E'
    else:
        return 'F'

percentage = float(input("Enter the student's percentage -> "))
grade = calculate_grade(percentage)
print(f"The student's grade is: {grade}")
